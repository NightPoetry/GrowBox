// 四国语言字典:zh-CN = 上面的 FALLBACK_ZH_CN(源,唯一中文真相);en/ja/zh-TW 由 locales/ 提供
// (Phase 4 由 deepseek 批量翻译填充,缺键回退 zh-CN)。切语言时 dict = { ...zh-CN 源, ...目标语言覆盖 }。
const LOCALES: Record<Lang, Record<string, string>> = {
  "zh-CN": FALLBACK_ZH_CN,
  en,
  ja,
  "zh-TW": zhTW,
};

/// 某语言的完整字典:以 zh-CN 为底、目标语言覆盖(缺键回退中文,优于裸 key)。
function mergedDict(l: Lang): Record<string, string> {
  return l === "zh-CN" ? FALLBACK_ZH_CN : { ...FALLBACK_ZH_CN, ...LOCALES[l] };
}

const [lang, setLangSignal] = createSignal<Lang>(initialLang());
const [dict, setDict] = createSignal<Record<string, string>>(mergedDict(initialLang()));

function initialLang(): Lang {
  if (typeof localStorage === "undefined") return "zh-CN";
  const v = localStorage.getItem(STORAGE_KEY);
  if (v === "en" || v === "ja" || v === "zh-CN" || v === "zh-TW") return v;
  return "zh-CN";
}

export function t(key: string): string {
  // 三层 fallback：dict（后端按当前语种返回）→ FALLBACK_ZH_CN（前端兜底）→ key 原字面。
  // 后端某语种缺译时退回中文，比 raw key 更友好；FALLBACK_ZH_CN 也缺则返回 key 让开发期看到。
  return dict()[key] ?? FALLBACK_ZH_CN[key] ?? key;
}

/** 翻译工具名:单一源 = 后端按 ui_lang 本地化的工具卡片 label(get_tools / tools.i18n.json)。
 *  兜底链:后端 label → 前端 tool_xxx 键(toolList 未加载时的临时兜底) → 原英文名。 */
export function tTool(name: string): string {
  const fromBackend = toolLabel(name);
  if (fromBackend) return fromBackend;
  const key = `tool_${name}`;
  return dict()[key] ?? FALLBACK_ZH_CN[key] ?? name;
}

export function currentLang(): Lang {
  return lang();
}

/// 切界面语言:只动 UI 字典,**不再**联动提示词语言(两轴解耦,用户决策 2026-06-02)。
export async function setLang(next: Lang): Promise<void> {
  setLangSignal(next);
  if (typeof localStorage !== "undefined") {
    localStorage.setItem(STORAGE_KEY, next);
  }
  await refreshDict();
}

// ── 提示词语言(给 LLM 的,中/英,独立于界面语言)──────────────
function initialPromptLang(): PromptLang {
  if (typeof localStorage === "undefined") return "zh";
  return localStorage.getItem(PROMPT_LANG_KEY) === "en" ? "en" : "zh";
}
const [promptLang, setPromptLangSignal] = createSignal<PromptLang>(initialPromptLang());

export function currentPromptLang(): PromptLang {
  return promptLang();
}

/// 切提示词语言:系统提示词 + 工具 schema description 改用该语言(后端 set_prompt_lang 归一+重载)。
export async function setPromptLang(next: PromptLang): Promise<void> {
  setPromptLangSignal(next);
  if (typeof localStorage !== "undefined") {
    localStorage.setItem(PROMPT_LANG_KEY, next);
  }
  if (tauriAvailable()) {
    await api.setPromptLang(next).catch(() => {});
  }
}

export async function refreshDict(): Promise<void> {
  if (!tauriAvailable()) {
    // 浏览器 dev 模式:用本地 locale 字典(zh-CN 底 + 目标语言覆盖)。
    setDict(mergedDict(lang()));
    return;
  }
  try {
    const fetched = await api.getTranslations(lang());
    // 合并:zh-CN 源 + 目标语言 locale 覆盖 + 后端下发覆盖(后端缺译的 key 仍回退中文,不退化为裸 key)。
    setDict({ ...mergedDict(lang()), ...fetched });
  } catch (e) {
    console.warn(`[i18n] getTranslations failed for ${lang()}:`, e);
    setDict(mergedDict(lang())); // 后端失败也要切到目标语言本地字典
  }
}

